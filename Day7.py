#Simple Calculator Using Python 

firstNumber = int(input("Enter First Number: "))
secondNumber = int(input("Enter Second Number: "))


print("Sum: ", firstNumber+secondNumber)
print("Minus: ", firstNumber-secondNumber)
print("Multiply: ", firstNumber*secondNumber)
print("Division: ", firstNumber/secondNumber)
print("Floated Division: ", firstNumber//secondNumber)
print("Remainder: ", firstNumber%secondNumber)
print("Exponent: ", firstNumber**secondNumber)



